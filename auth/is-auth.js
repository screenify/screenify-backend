const jwt = require("jsonwebtoken");
require("dotenv").config();


/**
 * Authorization Middleware
 * Authorized the uploader based on fixed token.
 */
module.exports = (req, res, next) => {
  const authHeader = req.get("Authorization");
  if (!authHeader) {
    req.isAuth = false;
    return next();
  }
  const token = authHeader.split(" ")[1]; // Authorization Bearer
  console.log("authorization header :", token);

  if (!token || token === "") {
    req.isAuth = false;
    return next();
  }
  let decodedToken;
  try {
    decodedToken = jwt.verify(token, process.env.jwtSecret);
  } catch (err) {
    req.isAuth = false;
    return next();
  }
  if (!decodedToken) {
    req.isAuth = false;
    return next();
  }
  req.isAuth = true;
  console.log("userId inside userId", decodedToken);
  next();
};